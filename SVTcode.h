//SVT single indel and special substitution correcting code

#ifndef __SVTcode_h__
#define __SVTcode_h__

#include "globalFile.h"
#include "myIO.h"
#include "Lcode.h"
#include "simulate.h"

bool SVT_valid_oligo(const VB &oligo);//check whether an oligo is a Levenshtein codeword interleaving a SVT codeword

int SVT_info_length();//number of information bits for one oligo

VB SVT_encode_binary(const VB &info);//SVT encoder, where the syndrome equals 0 (mod P) and weight is even
//correct single insertion/substitution that may happen at the i-th bit of code, i \in [left, right]
//correct single deletion that may happen just before the i-th bit of code, i \in [left, right]
VB SVT_decode_binary(const VB &noiseCode, int left, int right);
VB SVT_extract_info_binary(const VB &code);//extract infomation from binary SVT-codeword

VB SVT_encode(const VB &info);//L encode first part, SVT encode second part
VB SVT_decode(const VB &noiseOligo);//L decode first part, then SVT decode second part; return empty VB for failure
VB SVT_extract_info(const VB &oligo);//extract infomation from oligo

//read [read_size] many oligos uniformly at random from [write_oligo].
//each row is an extended encoded symbol of form (seed, xor_info, num).
//[seed] is the first integer identifying source symbols generating [xor_info],
//[xor_info] is a VI generated by xoring source symbols, and
//[num] is the last integer indicating the number of copies.
//negative (non-negative) num indicates valid but incorrect (correct) output
VVI SVT_read(const VVI &write_oligo);

#endif
